def call_deepseek_api(prompt: str, retries: int = 5, delay: float = 2.0):
    """Call DeepSeek API through requests"""

    headers = {
        "Authorization": f"Bearer {DEEPSEEK_API_KEY}",
        "Content-Type": "application/json"
    }

    payload = {
        "model": MODEL,
        "messages": [
            {
                "role": "user",
                "content": prompt
            }
        ],
        "max_tokens": 4000,
        "temperature": 0.1,
        "stream": False
    }

    for i in range(retries):
        try:
            response = requests.post(
                DEEPSEEK_API_URL,
                headers=headers,
                json=payload,
                timeout=60
            )

            if response.status_code != 200:
                raise Exception(f"API returned status {response.status_code}: {response.text}")

            response_data = response.json()

            if "choices" not in response_data or not response_data["choices"]:
                raise Exception(f"Unexpected response structure: {response_data}")

            content = response_data["choices"][0]["message"]["content"]

            # Extract usage if available
            usage = response_data.get("usage", {})

            if not content or content.strip() == "":
                raise ValueError("Received empty response from DeepSeek API")

            return content, usage  # Returns 2 values

        except requests.exceptions.RequestException as e:
            print(f"Network error on attempt {i+1}: {e}")
        except Exception as e:
            print(f"API error on attempt {i+1}: {e}")

        if i == retries - 1:
            raise Exception(f"Failed to get response from DeepSeek API after {retries} attempts")

        time.sleep(delay)
        delay *= 1.5

    return None, None

