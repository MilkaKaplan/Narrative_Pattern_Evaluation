def parse_response_simple(response):
    """Parsing (EXPECTED_MODELS = 1)"""

    model_match = re.search(
        r'^\s*Model:\s*(.+?)\s*$',
        response,
        re.IGNORECASE | re.MULTILINE
    )

    stage_total_match = re.search(
        r'^\s*StageTotal:\s*(\d+)\s*/\s*(\d+)\s*$',
        response, re.IGNORECASE | re.MULTILINE
    )
    coverage_match = re.search(
        r'^\s*Coverage:\s*([0-9]*\.?[0-9]+)\s*$',
        response, re.IGNORECASE | re.MULTILINE
    )

    score_match = re.search(
        r'^\s*Score:\s*([0-4])\s*$',
        response,
        re.IGNORECASE | re.MULTILINE
    )

    justification_match = re.search(
        r'^\s*Justification:\s*(.+)$',
        response,
        re.IGNORECASE | re.DOTALL | re.MULTILINE
    )

    models = []
    all_scores = {}

    if model_match and score_match:
        model_name = model_match.group(1).strip()
        score = int(score_match.group(1))
        justification = (
            justification_match.group(1).strip()
            if justification_match else ""
        )

        models.append({
            "model": model_name,
            "score": score,
            "justification": justification
        })
        all_scores[model_name] = score
        parse_error = None
    else:
        parse_error = "Model or Score not found in response"

    return {
        "models": models,
        "all_scores": all_scores,
        "raw_response": response,
        "parse_error": parse_error
    }

