def call_claude_with_caching(text: str, retries=3, delay=2.0) -> tuple:
    """Вызов Claude с prompt caching"""
    for i in range(retries):
        try:
            msg = client.messages.create(
                model=MODEL,
                temperature=0.2,
                max_tokens=8000,
                system=[




                    {
                        "type": "text",
                        "text": INSTRUCTIONS,
                        "cache_control": {"type": "ephemeral"}
                    }
                ],
                messages=[{
                    "role": "user",
                    "content": "TEXT TO ANALYZE:\n" + text
                }]
            )

            response = "".join(b.text for b in msg.content if b.type == "text")

            # Check all  MODELS
            model_count = response.count("Model:")
            if model_count < EXPECTED_MODELS:
                print(f"       Found  {model_count}/models")

                msg2 = client.messages.create(
                    model=MODEL,
                    temperature=0.2,
                    max_tokens=4000,
                    system=[{
                        "type": "text",
                        "text": INSTRUCTIONS,
                        "cache_control": {"type": "ephemeral"}
                    }],
                    messages=[
                        {
                            "role": "user",
                            "content":  "TEXT TO ANALYZE:\n" + text
                        },
                        {
                            "role": "assistant",
                            "content": response
                        },
                        {
                            "role": "user",
                            "content": "Please re-output strictly in the required format: Model / Score / Justification."
                        }
                    ]
                )

                response2 = "".join(b.text for b in msg2.content if b.type == "text")
                response = response + "\n\n" + response2

            usage = msg.usage
            cache_stats = {
                'cache_created': getattr(usage, 'cache_creation_input_tokens', 0),
                'cache_read': getattr(usage, 'cache_read_input_tokens', 0),
                'input_tokens': usage.input_tokens,
                'output_tokens': usage.output_tokens
            }

            return response, cache_stats


        except anthropic.RateLimitError:
            print(f"      ⏳ Rate limit, ждем {delay:.1f}s...")
            time.sleep(delay)
            delay *= 2
        except Exception as e:
            print(f"      ❌ Error (try again {i+1}/{retries}): {str(e)[:100]}")
            if i == retries - 1:
                return f"ERROR: {e}", None
            time.sleep(delay)

def calculate_cost(cache_stats, model_name):
    """Cost"""
    if not cache_stats:
        return 0.0

    # Price for different models
    if "sonnet" in model_name.lower():
        prices = {
            'cache_write_1h': 6.00,
            'cache_read': 0.30,
            'input': 3.00,
            'output': 15.00
        }
    elif "haiku" in model_name.lower():
        prices = {
            'cache_write_1h': 1.60,
            'cache_read': 0.08,
            'input': 0.80,
            'output': 4.00
        }
    else:  # Opus
        prices = {
            'cache_write_1h': 30.00,
            'cache_read': 1.50,
            'input': 15.00,
            'output': 75.00
        }

    cost = 0.0

    if cache_stats['cache_created'] > 0:
        cost += cache_stats['cache_created'] * prices['cache_write_1h'] / 1_000_000

    if cache_stats['cache_read'] > 0:
        cost += cache_stats['cache_read'] * prices['cache_read'] / 1_000_000

    cost += cache_stats['input_tokens'] * prices['input'] / 1_000_000
    cost += cache_stats['output_tokens'] * prices['output'] / 1_000_000

    return cost

