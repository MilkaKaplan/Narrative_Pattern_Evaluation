def call_gemini(text: str, retries=5, delay=5.0) -> tuple:
    """Call Gemini API with improved rate limit handling"""
    for i in range(retries):
        try:
            model = genai.GenerativeModel(
                model_name=MODEL_NAME,
                generation_config={
                    "temperature": 0.2,
                    "max_output_tokens": 8000,
                }
            )
            # Combine instructions and text
            prompt = f"{INSTRUCTIONS}\n\nTEXT TO ANALYZE:\n{text}"

            # Call the model
            response = model.generate_content(prompt)

            # Extract response text
            response_text = response.text

            # Check number of models
            model_count = response_text.count("Model:")
            if model_count < EXPECTED_MODELS:
                print(f"      Found {model_count}/{EXPECTED_MODELS} models, retrying...")
                time.sleep(2)  # Short pause before retry

                # Retry with clarification
                #follow_up = f"{prompt}\n\nPrevious response:\n{response_text}\n\nPlease re-output strictly in the required format with EXACT keys: Model, StageTotal, Coverage, Score, Justification. No extra text."
                follow_up = "RE-OUTPUT STRICTLY IN THE REQUIRED FORMAT. NO EXTRA TEXT."
                response2 = model.generate_content(follow_up)
                response_text = response_text + "\n\n" + response2.text

            # Token count (approximate)
            usage_stats = {
                'input_tokens': len(prompt.split()) * 1.3,
                'output_tokens': len(response_text.split()) * 1.3,
                'cache_created': 0,
                'cache_read': 0
            }

            return response_text, usage_stats

        except Exception as e:
            error_msg = str(e)

            # Check for rate limit or quota errors
            if any(keyword in error_msg.lower() for keyword in ["429", "quota", "resource", "rate limit", "exhausted"]):
                if i < retries - 1:  # Not the last attempt
                    wait_time = delay * (2 ** i)  # Exponential backoff
                    print(f"      Rate limit (attempt {i+1}/{retries}), waiting {wait_time:.1f}s...")
                    time.sleep(wait_time)
                else:
                    print(f"      Rate limit exceeded after {retries} retries")
                    return f"ERROR: Rate limit - {e}", None
            else:
                print(f"      Error (attempt {i+1}/{retries}): {error_msg[:200]}")
                if i == retries - 1:
                    return f"ERROR: {e}", None
                time.sleep(3)

    return "ERROR: Max retries exceeded", None

def parse_response_satire(response: str):
    """
    Robust parser for the STRICT format you ask Gemini to output:
    - Model: ...
    - Stage 1 (Setting): [0/1/2] - ...
    ...
    - StageTotal: S/16
    - Coverage: C
    - Score: 0-4
    - Justification: ...
    Returns: (data_dict, error_string_or_None)
    """

    # 1) Normalize common markdown / formatting noise
    text = response.replace("\r\n", "\n")
    # remove bold markers but keep content
    text = text.replace("**", "")
    # Sometimes Gemini adds code fences
    text = re.sub(r"```.*?\n", "", text)
    text = text.replace("```", "")

    data = {}

    # 2) Core fields
    model_match = re.search(r'^\s*Model:\s*(.+?)\s*$', text, re.MULTILINE | re.IGNORECASE)
    if not model_match:
        return None, "Parse error: missing Model"

    data["model"] = model_match.group(1).strip()

    # StageTotal: accept "10/16" or "10 / 16"
    stage_total_match = re.search(
        r'^\s*StageTotal:\s*(\d+)\s*/\s*16\s*$',
        text, re.MULTILINE | re.IGNORECASE
    )
    if not stage_total_match:
        return None, "Parse error: missing StageTotal (expected '/16')"

    data["stage_total"] = int(stage_total_match.group(1))

    # Coverage: accept decimal, optionally followed by anything in parentheses
    coverage_match = re.search(
        r'^\s*Coverage:\s*([0-9]*\.?[0-9]+)\s*(?:\([^)]*\))?\s*$',
        text, re.MULTILINE | re.IGNORECASE
    )
    if not coverage_match:
        return None, "Parse error: missing Coverage"

    data["coverage"] = float(coverage_match.group(1))

    score_match = re.search(r'^\s*Score:\s*([0-4])\s*$', text, re.MULTILINE | re.IGNORECASE)
    if not score_match:
        return None, "Parse error: missing Score"

    data["score"] = int(score_match.group(1))

    just_match = re.search(
        r'^\s*Justification:\s*(.+?)\s*$',
        text, re.MULTILINE | re.IGNORECASE
    )
    # Justification may be multi-line; if so, capture everything after "Justification:"
    if not just_match:
        just_match2 = re.search(
            r'^\s*Justification:\s*(.*)$',
            text, re.MULTILINE | re.IGNORECASE
        )
        if just_match2:
            # take from that point to end, but strip trailing noise
            start = just_match2.start()
            data["justification"] = text[start:].split(":", 1)[1].strip()
        else:
            data["justification"] = ""
    else:
        data["justification"] = just_match.group(1).strip()

    # 3) Stages 1..8
    # Accept:
    #  - Stage 1 (Setting): [2] - ...
    #  - Stage 1 (Setting): 2 - ...
    #  - Stage 1: 2
    stage_scores = {}
    for i in range(1, 9):
        # Try bracket form first
        m = re.search(
            rf'^\s*Stage\s*{i}\s*(?:\([^)]*\))?\s*:\s*\[\s*([0-2])\s*\]\s*(?:-\s*.*)?$',
            text, re.MULTILINE | re.IGNORECASE
        )
        if not m:
            # Try plain digit form
            m = re.search(
                rf'^\s*Stage\s*{i}\s*(?:\([^)]*\))?\s*:\s*([0-2])\s*(?:-\s*.*)?$',
                text, re.MULTILINE | re.IGNORECASE
            )
        if not m:
            return None, f"Parse error: Stage {i} missing or not parseable"

        stage_scores[i] = int(m.group(1))
        data[f"stage_{i}"] = stage_scores[i]

    # 4) Optional sanity check: StageTotal equals sum of stages
    ssum = sum(stage_scores.values())
    if ssum != data["stage_total"]:
        # Not fatal, but you may want to treat as error:
        # return None, f"Parse error: StageTotal mismatch (got {data['stage_total']} vs sum {ssum})"
        data["stage_total_mismatch"] = f"declared={data['stage_total']}, sum={ssum}"
        # prefer the sum (more reliable)
        data["stage_total"] = ssum
        data["coverage"] = ssum / 16.0

    return data, None

def analyze():
    txt_files = sorted(Path(INPUT_FOLDER).glob("*.txt"))
    results_file = Path(OUTPUT_FOLDER) / "satire_results.csv"

    raw_folder = Path(OUTPUT_FOLDER) / "raw_responses"
    raw_folder.mkdir(exist_ok=True)

    # --- RESUME: load already processed filenames from CSV (if exists)
    processed = set()
    if results_file.exists():
        try:
            df_old = pd.read_csv(results_file)
            if "filename" in df_old.columns:
                processed = set(df_old["filename"].astype(str).tolist())
            print(f"✅ Resume: already processed {len(processed)} files from {results_file.name}")
        except Exception as e:
            print(f"⚠️ Resume: couldn't read existing CSV ({results_file.name}): {e}")

    # We'll collect only NEW rows in memory, and append/merge periodically
    new_rows = []
    start_time = time.time()
    processed_this_run = 0
    errors = 0

    total_files = len(txt_files)
    to_process = [p for p in txt_files if p.name not in processed]
    print(f" Total .txt files: {total_files}")
    print(f" To process now: {len(to_process)}")
    print(f"⏭  Skipping: {total_files - len(to_process)}\n")

    for idx, txt_file in enumerate(to_process, 1):
        print(f"[{idx}/{len(to_process)}] {txt_file.name}")

        try:
            text = txt_file.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            # fallback
            text = txt_file.read_text(encoding="utf-8", errors="replace")

        word_count = len(text.split())
        print(f"   📝 Words: {word_count}")

        response_text, usage = call_gemini(text)

        if isinstance(response_text, str) and response_text.startswith("ERROR"):
            print(f"   ❌ Gemini error: {response_text}")
            errors += 1
            continue

        # --- Save raw response (stable name, not dependent on idx)
        raw_path = raw_folder / f"{txt_file.stem}_raw.txt"
        try:
            raw_path.write_text(response_text, encoding="utf-8")
        except Exception as e:
            print(f"   ⚠️ Could not write raw response: {e}")

        parsed, error = parse_response_satire(response_text)
        if error:
            print(f"   ❌ {error}")
            errors += 1
            continue

        row = {
            "filename": txt_file.name,
            "word_count": word_count,
            "model": parsed["model"],
            "score": parsed["score"],
            "stage_total": parsed["stage_total"],
            "coverage": parsed["coverage"],
            "justification": parsed["justification"],
        }
        for i in range(1, 9):
            row[f"stage_{i}"] = parsed[f"stage_{i}"]

        new_rows.append(row)
        processed_this_run += 1

        print(f"    Score: {parsed['score']} | Coverage: {parsed['coverage']:.2f}")

        # --- Save progress every 5 NEW successful rows
        if processed_this_run % 5 == 0:
            df_new = pd.DataFrame(new_rows)

            if results_file.exists():
                try:
                    df_old = pd.read_csv(results_file)
                    df_all = pd.concat([df_old, df_new], ignore_index=True)
                except Exception as e:
                    print(f"   ⚠️ Could not merge with existing CSV, overwriting. Reason: {e}")
                    df_all = df_new
            else:
                df_all = df_new

            # Deduplicate by filename (keep last)
            if "filename" in df_all.columns:
                df_all = df_all.drop_duplicates(subset=["filename"], keep="last")

            df_all.to_csv(results_file, index=False)
            new_rows = []
            print("   Saved progress\n")

        time.sleep(2)

    # --- Final save (remaining rows)
    if new_rows:
        df_new = pd.DataFrame(new_rows)
        if results_file.exists():
            try:
                df_old = pd.read_csv(results_file)
                df_all = pd.concat([df_old, df_new], ignore_index=True)
            except Exception as e:
                print(f"⚠️ Could not merge with existing CSV at final save, overwriting. Reason: {e}")
                df_all = df_new
        else:
            df_all = df_new

        if "filename" in df_all.columns:
            df_all = df_all.drop_duplicates(subset=["filename"], keep="last")

        df_all.to_csv(results_file, index=False)
        print("💾 Final save complete")

    elapsed = (time.time() - start_time) / 60
    print(f"\nDone in {elapsed:.1f} minutes")
    print(f" Newly processed (successful): {processed_this_run}")
    print(f" Errors: {errors}")
    print(f" Results: {results_file}")
    print(f" Raw responses folder: {raw_folder}")

if __name__ == "__main__":
  analyze()

