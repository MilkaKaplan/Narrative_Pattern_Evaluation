# --- pre ---
id_col = "title of novella"
df = df.rename(columns=lambda c: c.strip())

feature_cols = [c for c in df.columns if c != id_col]
X = df[feature_cols].astype(float).values

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

n_features = X.shape[1]

# --- alpha from MultiTaskLassoCV ---
from sklearn.linear_model import MultiTaskLassoCV
mtcv = MultiTaskLassoCV(cv=5, random_state=42, max_iter=200000)
mtcv.fit(X_scaled, X_scaled)
alpha_star = mtcv.alpha_

print("Chosen alpha:", alpha_star)

# --- stability by subsampling ---
n_runs = 200
sample_frac = 0.75

selected_counts = np.zeros(n_features)
importance_sum = np.zeros(n_features)

rng = np.random.default_rng(42)

for _ in range(n_runs):
    idx = rng.choice(len(X_scaled), size=int(sample_frac * len(X_scaled)), replace=False)
    X_sub = X_scaled[idx]

    model = MultiTaskLasso(alpha=alpha_star, max_iter=200000)
    model.fit(X_sub, X_sub)

    coef = model.coef_
    imp = np.linalg.norm(coef, axis=0)

    selected_counts += (imp > 1e-8)
    importance_sum += imp

stability_df = pd.DataFrame({
    "feature": feature_cols,
    "selection_rate": selected_counts / n_runs,
    "mean_importance": importance_sum / n_runs
}).sort_values(
    ["selection_rate", "mean_importance"],
    ascending=False
).reset_index(drop=True)

