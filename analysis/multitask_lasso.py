def stage_concept(col):
    # comedy_2_Catalyst -> 2_Catalyst
    # mystery_1_Introduction... -> 1_Introduction...
    m = re.match(r"^(satire|comedy|romance|mystery|tragedy)_(\d+)_(.*)$", col, flags=re.IGNORECASE)
    if m:
        return f"{m.group(2)}_{m.group(3)}"
    return col

